// == (Cmparação implícita) => Compara apenas o valor

const numero = 5;
const texto = '5';

console.log(numero == texto);

// === (Compara valor e tipo) => Compara o valor da variável e o tipo da mesma

console.log(numero === texto);
console.log(typeof numero);
console.log(typeof texto);

